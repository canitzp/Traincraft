package de.canitzp.traincraft.tile;

/**
 * @author canitzp
 */
public class TranslationPack {

    private double x, y, z;

    public TranslationPack(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}


